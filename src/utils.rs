use crate::fieldelement::*;
use num_bigint::{BigUint, ToBigUint};
use std::str::FromStr;

pub fn field_element<T: ToBigUint>(value: T) -> FieldElement {
    FieldElement::new(value.to_biguint().unwrap())
}

pub fn felt_from_str(value: &str) -> FieldElement {
    FieldElement::new(BigUint::parse_bytes(value.as_bytes(), 10).unwrap())
}

// As we work under prime field, there always exists multiplicative inverse. So, no need to wrap output with Option.
pub fn ext_euclid(a: &FieldElement, b: &FieldElement) -> FieldElement {
    let mut a = a.clone();
    let mut b = b.clone();
    if gcd(&a, &b) != 1 {
        return field_element(0);
    } else {
        let mut s = field_element(1);
        let mut s_old = field_element(0);
        let mut t = field_element(0);
        let mut t_old = field_element(1);

        while b != 0 {
            let q = a.clone();
            a = b.clone();
            b = &a % &b;
            let temp = s.clone();
            s = s_old - &q * &s;
            s_old = temp;
            let temp1 = t.clone();
            t = t_old - q * &t;
            t_old = temp1;
        }

        if s_old < field_element(0) {
            s_old += b.clone();
            return s_old;
        } else {
            return s_old;
        }
    }
}

pub fn gcd(a: &FieldElement, b: &FieldElement) -> FieldElement {
    let mut a = a.clone();
    let mut b = b.clone();
    while b != 0 {
        let t = b.clone();
        b = a % b;
        a = t;
    }
    return a;
}

#[allow(non_snake_case)]
pub fn new_BigUint<T: ToBigUint>(num: T) -> BigUint
where
    BigUint: From<T>,
{
    num.into()
}

#[allow(non_snake_case)]
pub fn BigUint(x: &str) -> BigUint {
    BigUint::parse_bytes(x.as_bytes(), 10).expect("cannot create BigUint from the gn input")
}

// LSB at index 0
pub fn to_bits(num: BigUint) -> Vec<u8> {
    let mut bits = vec![0; 256];
    let mut k = num.clone();

    for i in 0..256 {
        let bit = &k & new_BigUint(1u32);
        k /= new_BigUint(2_u32);

        bits[i] = u8::from_str(&bit.to_string()).unwrap();
    }
    bits
}

